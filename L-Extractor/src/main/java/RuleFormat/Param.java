package RuleFormat;

public class Param {
    private String paramType;
    private String paramFormal;

    public String getParamType() {
        return paramType;
    }

    public void setParamType(String paramType) {
        this.paramType = paramType;
    }

    public String getParamFormal() {
        return paramFormal;
    }

    public void setParamFormal(String paramFormal) {
        this.paramFormal = paramFormal;
    }
}
